static void Gen_SineTable(void)
{
  for(uint16_t i = 0; i < SINE_TABLE_SIZE; i++)
  {
    float val = sin(2.0f * M_PI * i / SINE_TABLE_SIZE);
    sine_table[i] = (uint16_t)(2048 + 2047 * val);
  }
}

